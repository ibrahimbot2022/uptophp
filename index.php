            <div class="sectiontag"><h3>ABOUT US</h3></div>
            <div class="sectionsubtag"><p>Libraries are established for the systematic collection, organization, preservation and dissemination of knowledge and information. It is very important for man to preserve and maintain the valuable knowledge and information contained in the books and documents because we want to preserve our knowledge and wisdom for the coming generations. By preserving the documents in a library this knowledge can be made available to others so that they can benefit from it.

                Libraries play a vital role in the development of any society by enhancing the cause of education and academic research. They cater to the information needs of thousands of peoples.</p>
                <br/>
                <h3>LOCATION</h3>
                <br/>
                <p>The library is aesthetically located in a central place of the college and is very prominently housed in a big and spacious building with a floor area of 4599 Sq. mts (49500 Sq.ft).</p>
                <br/>
                <h3>MISSION</h3>
                <br/>
                <p>The mission of BMSCE Library is to seek, create, communicate and preserve knowledge and understanding and carryout the mission in a unified community known as College.
                
                The library, in support of the educational and research mission of the college, is the local repository and the principal gateway to current information and the scholarly record.
                
                As such, it is simultaneously collection and connection for the current and future students and faculty of the college.</p>
                
                <br/>
                <h3>VISION</h3>
                <br/>
                <p>It is an information hub of academic community and a center of unbridled intellectual inquiry. A place where the past converges with the present and the road to the future is littered with every imaginable tried ideas and tested theory.</p>
                <br/>
                <h3>OBJECTIVE</h3>
                <br/>
                <p>The primary objective of the library is to offer a fully integrated and dynamic environment suited for academic pursuits, study and research for the students and staff, by augmenting traditional print and electronic resources both local and global technologies.</p>
                <br/>
                <h3>E-LIBRARY FACILITIES</h3>
                <br/>
                <p>The Digital library provides On-line access to 11 e-resources on Science and Technology for studies and research, and has access to 7472 e-journals & 14585 e-books on various branches of Science, Technology, Engineering &, Management. Library also subscribes 160 National and International printed journals.
                Provide access to all streams of Question Papers through Digital Software–TechFocuz via Intranet.
                Subscribed to NPTEL lectures are streamed using Intranet</p>
